package com.huawei.padresourcepool.activity;

import java.io.Serializable;

/**
 * Created by tWX366549 on 2017/2/17.
 */

public class IjkOptions implements Serializable {
    private int opt_category;
    private String opt_name;
    private String opt_value;

    public IjkOptions(int opt_category, String opt_name, String opt_value) {
        this.opt_category = opt_category;
        this.opt_name = opt_name;
        this.opt_value = opt_value;
    }

    public int getOpt_category() {
        return opt_category;
    }

    public void setOpt_category(int opt_category) {
        this.opt_category = opt_category;
    }

    public String getOpt_name() {
        return opt_name;
    }

    public void setOpt_name(String opt_name) {
        this.opt_name = opt_name;
    }

    public String getOpt_value() {
        return opt_value;
    }

    public void setOpt_value(String opt_value) {
        this.opt_value = opt_value;
    }

    @Override
    public String toString() {
        return "IjkOptions{" +
                "opt_category=" + opt_category +
                ", opt_name='" + opt_name + '\'' +
                ", opt_value='" + opt_value + '\'' +
                '}';
    }
}
